const mongoose = require('mongoose');

const UserSchema = new mongoose.Schema({
  image: {
    type: Buffer,
    required: false,
    contentType: String,
  },
  name: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true
  },
  password: {
    type: String,
    required: true
  },
  date: {
    type: Date,
    default: Date.now
  }
});

const User = mongoose.model('User', UserSchema);

module.exports = User;
